# n-1 + n-2 + n-3
n = int(input())
for _ in range(n):
  arr = [0,1,2,4]
  m = int(input())

  if m > 3:
    for i in range(4,m+1):
      arr.append(arr[i-1]+arr[i-2]+arr[i-3])

  print(arr[m])